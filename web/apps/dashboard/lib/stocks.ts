/** 이름(contains) 또는 코드(startsWith)로 검색 */
export function searchStocks(query: string, limit = 8): StockEntry[] {
  const q = query.trim();
  if (!q) return [];
  const isCode = /^\d+$/.test(q);
  return KRX_STOCKS.filter((s) =>
    isCode ? s.ticker.startsWith(q) : s.name.includes(q) || s.ticker.startsWith(q),
  ).slice(0, limit);
}
